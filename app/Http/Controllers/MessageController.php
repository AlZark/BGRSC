    {
        if(!StatisticController::checkIfCreated())
        {
            $message = new Message();
            $message->subject = 'Reminder to report animal cases';
            $message->content = 'Please report statistics for ' . date('Y-m');
            $message->user_id = 1;

            $message->save();
        }
    }

